public void carga_datos(){
     // Llama al m√©todo del servicio web para obtener la lista de consultores
     // Llama al m√©todo del servicio web para obtener la lista de clientes
    List<Cliente> clientes = servicioWeb.listarClientes();

    // Verifica si la lista de clientes no est√° vac√≠a
    if (clientes != null && !clientes.isEmpty()) {
        // Obtiene el modelo de la tabla
        DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();

        // Limpia la tabla antes de agregar nuevos datos
        model.setRowCount(0);

        // Agrega los datos de los clientes al modelo de datos de la tabla
        for (Cliente cliente : clientes) {
            Object[] rowData = {cliente.getRut(), cliente.getNombre(), cliente.getCorreoelectronico(), cliente.getIdentificadorempresa(), cliente.getRazonsocial()};
            model.addRow(rowData);
        }
    } else {
        // Si la lista de clientes est√° vac√≠a o hay un error al obtener los datos, muestra un mensaje de error
        JOptionPane.showMessageDialog(this, "Error al obtener datos de clientes desde el servicio web.", "Error", JOptionPane.ERROR_MESSAGE);

        }       
   }    
